##COMO UTILIZAR O PRINT
variável = 0
print("Texto")
print(variável)
print("Texto", variável)  ##Output é "Texto variável" (barra de espaço acrescentada). Variável pode ser de qualquer tipo
print(f"Texto {variável}")  ##F-String para formatação de variável
print("Texto" + variável) ##Concatenação de string. Só acontece com string.
print(5)
print(5.0)
print(5 + 4)  ##Operação matemática de tipos numéricos iguais
print(5 + 4.0)  ##Operação matemática de tipos numéricos diferentes

##TIPOS DE VARIÁVEIS
int ##INTEIRO
float ##FLOAT
str ##STRING